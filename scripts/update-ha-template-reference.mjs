#!/usr/bin/env node
import { mkdirSync, writeFileSync } from "node:fs";
import { dirname } from "node:path";

const OUT = "docs/reference/ha-template-reference.md";
// Raw markdown from GitHub — pinnable per branch/tag
const SRC =
  "https://raw.githubusercontent.com/home-assistant/home-assistant.io/current/source/_docs/configuration/templating.markdown";
const DATE = new Date().toISOString().slice(0, 10);

console.log("Fetching HA template docs from GitHub...");

const res = await fetch(SRC);
if (!res.ok) {
  console.error(`Fetch failed: ${res.status} ${res.statusText}`);
  process.exit(1);
}
const body = await res.text();

mkdirSync(dirname(OUT), { recursive: true });

const header = [
  `<!-- Auto-generated by scripts/update-ha-template-reference.mjs -->`,
  `<!-- Source: ${SRC} -->`,
  `<!-- Updated: ${DATE} -->`,
  "",
].join("\n");

writeFileSync(OUT, header + body);

const lines = body.split("\n").length;
console.log(`Written to ${OUT} (${lines} lines)`);
